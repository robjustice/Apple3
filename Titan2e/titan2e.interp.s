            .segment "RAM"
            .export  SLOT5ROM
            .export  SLOT6ROM
            .export  SLOT7ROM
            .export  TWOEROM            
 
            .org     $2000 - 14
; sos interp header
            .byte    "SOS NTRP"
            .word    0000
            .word    CODEST
            .word    CODELEN


CODEST:     lda     #$18       ;set zero page to $18
            sta     Z_REG
            ldx     #$00       ;disable extended addressing for SRCPTR & DSTPTR
            stx     L1401
            stx     L1403

; copy the code from the interp at CODE0C00 to $0C00 - $13FF
            lda     #$00
            sta     DSTPTR
            lda     #$0C
            sta     DSTPTR+1
            lda     #<CODE0C00
            sta     SRCPTR
            lda     #>CODE0C00
            sta     SRCPTR+1
            ldx     #$13
            jsr     MOVEBLK
            
; copy the code from the interp at CODE0C00+$E00 to $1A00 - $1FFF
            lda     #$00
            sta     DSTPTR
            lda     #$1A
            sta     DSTPTR+1
            lda     #<(CODE0C00+$E00)
            sta     SRCPTR
            lda     #>(CODE0C00+$E00)
            sta     SRCPTR+1
            ldx     #$1F
            jsr     MOVEBLK

; copy 9000 - bfff
; this is copied using extended addressing
; 9000-9fff to bank0  (from current bank)
; A000-bfff in system bank
            lda     #$00
            sta     DSTPTR
            lda     #$90
            sta     DSTPTR+1
            lda     #<CODE9000
            sta     SRCPTR
            lda     #>CODE9000
            sta     SRCPTR+1
            lda     #$8f         ;store using special 8f case extended addressing
            sta     L1403
            ldx     #$bf
            jsr     MOVEBLK

; some init/test code from the original boot sector
; -need to work out what its doing, something with the language card
;
@LA07F:     lda     ENV_REG
            pha                ;save env_reg
            lda     #$fc       ;(1.I.S.R:W_P.R.R)
            sta     ENV_REG    ; 1Mhz,Cxxx I/O enab,video on, NMI/Reset enab,write prot ram off?,prim stack,no rom
            lda     Z_REG
            ldx     #$18
            stx     Z_REG
            pha                ;save original zeropage reg
            lda     #$8f       ;enable special 8f ext addr
            sta     $1401
            lda     #$00       ;set 00 to $D000
            sta     $00
            lda     #$d0
            sta     $01
            ldy     #$00
            ldx     #$df
            lda     $c300        ;enable 3plus2e card (its always in slot3)
            lda     LCBANK2
            lda     #$00
@LA0AA:     sta     SETSTDZP     ;iie - set standard zero page
            sta     SETSLOTCXROM ;iie - 
            sta     LCBANK2      ;enable language card rw bank2
            sta     ($00),y      
            sta     SETALTZP
            sta     ($00),y
            sta     SETINTCXROM
            sta     ($00),y
            sta     SETSTDZP
            sta     ($00),y
            sta     SETSLOTCXROM  ;iie - reset intcxrom
            cpx     $01
            bcc     @LA0DF
            sta     DRV1EN
            sta     ($00),y
            sta     SETALTZP      ;iie - set altzp
            sta     ($00),y
            sta     SETINTCXROM
            sta     ($00),y
            sta     SETSTDZP
            sta     ($00),y
@LA0DF:     iny
            bne     @LA0AA
            inc     $01
            bne     @LA0AA
            sta     ROMIN_RO
            pla                ;restore zero page
            sta     Z_REG
            pla                ;restore env reg
            sta     ENV_REG


            jmp     $0C00

; copy memory from SRCPTR to DSTPTR
MOVEBLK:    ldy     #0
CC1:        lda     (SRCPTR),Y
            sta     (DSTPTR),Y
            iny
            bne     CC1
            inc     DSTPTR+1
            inc     SRCPTR+1
            txa
            cmp     DSTPTR+1
            bcs     CC1
            rts

            .res    $2100 - *,0
 
