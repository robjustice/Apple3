; This is based on a disassembly of the Titan //e disk and then refactored into
; a SOS Interpreter to allow lauching of the //e emulation from a Selector menu.
; The Interpreter is loaded, and then the contents are moved around in memory
; to setup the environment that the boot loader did on the original //e disk
;
;
; Interpreter code layout
; 2000 - 20FF  - memory move and card init routines
; 2100 - 21FF  - Slot 5 rom image, no physical slot on the ///, ram here
; 2200 - 22FF  - Slot 6 rom image, no physical slot on the ///, ram here
; 2300 - 23FF  - Slot 7 rom image, no physical slot on the ///, ram here
; 2400 - 53FF  - TWOE rom image (basic, etc)
; 5400 - 57FF  - font data 
; 5800 - 63FF  - ???
; 6400 - 6BFF  - alternate Cxxx rom image, this is the //e alternate Cxxx firmware
; 6C00 - 83FF  -
; 8400 - 97FF  - actual emulation init code and menus
;
;
; Memory map (after code is moved into correct locations)
; 0C00 - 0DFF  - copy slot and applesoft roms into correct memory locations
; 0E00 - 13FF  - start menu and config menu things
; 1A00 - 1FFF  - menu stuff continued here
;
; 9000 - 93FF  - this is the IIe font that is loaded. Sits in bank0
; 9400 - 9FFF  - this is in bank 0, not sure what this is
; A000 - BFFF  - continues up to bfff. we copy it into place
;
;
;
;    (1.I.S.R:W_P.R.R) ::= ENVIRONMENT REGISTER FLAGS.  FROM LEFT TO RIGHT BITS 7..0 
;                          (1MHZ, I/O ENABLE, SCREEN ENABLE, RESET ENABLE, 
;                           WRITE PROTECT, PRIMARY STACK, ROM1, ROM ENABLE)
;


